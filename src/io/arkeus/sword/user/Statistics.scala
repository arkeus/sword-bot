package io.arkeus.sword.user

class Statistics {
	var strength: Int = 10
	var defense: Int = 10
	var wisdom: Int = 10
	var agility: Int = 10
	var stamina: Int = 10
	var spirit: Int = 10
	
	override def toString = s"''STR'' $strength | ''DEF'' $defense | ''WIS'' $wisdom | ''AGI'' $agility | ''STA'' $stamina | ''SPI'' $spirit"
}